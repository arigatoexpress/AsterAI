    axes[0, 0].legend()

    # Add value labels
    for bar, p_val in zip(bars, p_values):
        axes[0, 0].text(bar.get_x() + bar.get_width()/2, p_val + 0.03,
                        '.3f', ha='center', va='bottom', color='white', fontweight='bold')

    # Distribution analysis
